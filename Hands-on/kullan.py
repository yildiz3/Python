cap=input("Dairenin capi:")
#kullanicadan dairenin capini girmesini istiyoruz

yaricap=int(cap)/2

#pi sayimiz sabit
pi=3.14159

#yukaridaki bilgileri kullanarak dairenin alanini hesaplayabiliriz.
alan=pi*(yaricap*yaricap)

#son olarak hesapladigimiz alani yaziyoruz
print("Capi",cap,"cm olan dairenin alani:",alan,"cm2'dir")
